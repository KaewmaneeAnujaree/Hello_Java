class Basic_operator{
	public static void main(String[] args){
		int number1 = 10;
		int number2 = 5;
		System.out.println("10 + 5 = "+ (number1+number2));
		System.out.println("10 - 5 = "+(number1-number2));
		System.out.println("10 * 5 = "+(number1*number2));
		System.out.println("10 / 5 = "+(number1/number2));	
	}
}
